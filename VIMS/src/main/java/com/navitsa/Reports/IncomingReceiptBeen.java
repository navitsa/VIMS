package com.navitsa.Reports;

public class IncomingReceiptBeen {

	private String invno;
	private String vecno;
	private String inctotal;
	private String payamount;
	private String balance;
	private boolean style;
	
	public String getInvno() {
		return invno;
	}
	public void setInvno(String invno) {
		this.invno = invno;
	}
	public String getVecno() {
		return vecno;
	}
	public void setVecno(String vecno) {
		this.vecno = vecno;
	}
	public String getInctotal() {
		return inctotal;
	}
	public void setInctotal(String inctotal) {
		this.inctotal = inctotal;
	}
	public String getPayamount() {
		return payamount;
	}
	public void setPayamount(String payamount) {
		this.payamount = payamount;
	}
	public boolean isStyle() {
		return style;
	}
	public void setStyle(boolean style) {
		this.style = style;
	}
	public String getBalance() {
		return balance;
	}
	public void setBalance(String balance) {
		this.balance = balance;
	}
	
	
}
