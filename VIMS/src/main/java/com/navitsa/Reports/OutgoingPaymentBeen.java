package com.navitsa.Reports;

public class OutgoingPaymentBeen {

	private String glaccno;
	private String glaccname;
	private String amount;
	private String remarks;
	private boolean style;
	
	public String getGlaccno() {
		return glaccno;
	}
	public void setGlaccno(String glaccno) {
		this.glaccno = glaccno;
	}
	public String getGlaccname() {
		return glaccname;
	}
	public void setGlaccname(String glaccname) {
		this.glaccname = glaccname;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public boolean isStyle() {
		return style;
	}
	public void setStyle(boolean style) {
		this.style = style;
	}
	
	
	
}
