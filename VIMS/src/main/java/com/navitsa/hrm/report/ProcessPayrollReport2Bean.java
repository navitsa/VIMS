package com.navitsa.hrm.report;

public class ProcessPayrollReport2Bean {

	private String empID;
	private String name;
	private String basicSalary;
	private String budAllo;
	private String attAllo;
	public String getEmpID() {
		return empID;
	}
	public void setEmpID(String empID) {
		this.empID = empID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBasicSalary() {
		return basicSalary;
	}
	public void setBasicSalary(String basicSalary) {
		this.basicSalary = basicSalary;
	}
	public String getBudAllo() {
		return budAllo;
	}
	public void setBudAllo(String budAllo) {
		this.budAllo = budAllo;
	}
	public String getAttAllo() {
		return attAllo;
	}
	public void setAttAllo(String attAllo) {
		this.attAllo = attAllo;
	}
	
	
}
